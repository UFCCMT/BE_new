#include<data_structures>
#include<semaphore.h>
#include<iostream>
#include<fstream>





//------------------------------------------------------------------------------
//-----------------------Shared memory constructor------------------------------
//------------------------------------------------------------------------------

shared_memory::shared_memory()
{
  completion=0;
}










//------------------------------------------------------------------------------
//-------------------------Function to increment completion---------------------
//------------------------------------------------------------------------------

void shared_memory::increment_completion()
{
  sem_wait(&shmem_sem);
  completion++;
  sem_post(&shmem_sem);
}





//------------------------------------------------------------------------------
//-------------------------Function to get completion---------------------------
//------------------------------------------------------------------------------

int16_t shared_memory::get_completion()
{
  return completion;
}





